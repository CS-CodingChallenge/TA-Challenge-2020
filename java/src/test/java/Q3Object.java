import skeleton.helpers.Edge;

class Q3Object {
    int numNodes;
    Edge[] edges;

    public Q3Object() {
    }

    public int getNumNodes() {
        return numNodes;
    }

    public void setNumNodes(int numNodes) {
        this.numNodes = numNodes;
    }

    public Edge[] getEdges() {
        return edges;
    }

    public void setEdges(Edge[] edges) {
        this.edges = edges;
    }
}
