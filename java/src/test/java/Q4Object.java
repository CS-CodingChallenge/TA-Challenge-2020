class Q4Object {
    String[][] rows;
    int numberMachines;

    public Q4Object() {
    }

    public String[][] getRows() {
        return rows;
    }

    public void setRows(String[][] rows) {
        this.rows = rows;
    }

    public int getNumberMachines() {
        return numberMachines;
    }

    public void setNumberMachines(int numberMachines) {
        this.numberMachines = numberMachines;
    }
}
