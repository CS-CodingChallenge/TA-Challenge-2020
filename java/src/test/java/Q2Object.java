class Q2Object {
    int[] cashFlowIn;
    int[] cashFlowOut;

    public Q2Object() {
    }

    public int[] getCashFlowIn() {
        return cashFlowIn;
    }

    public void setCashFlowIn(int[] cashFlowIn) {
        this.cashFlowIn = cashFlowIn;
    }

    public int[] getCashFlowOut() {
        return cashFlowOut;
    }

    public void setCashFlowOut(int[] cashFlowOut) {
        this.cashFlowOut = cashFlowOut;
    }
}
